      SUBROUTINE M38
      IMPLICIT REAL*8(A-H,O-Z),LOGICAL(L)
      CHARACTER*98 CARDH
      CHARACTER*78 CARDD
      CHARACTER*80 CARDQ
!
      DO I=1,80
        CARDQ(I:I)='&'
      ENDDO
!
   10 CONTINUE
      READ(36,5000,END=100) CARDH
      WRITE(38,5002) CARDQ 
      WRITE(38,5000) CARDH 
   20 CONTINUE
      READ(37,5001,END=90) CARDD
      IF(CARDD(3:12).NE.CARDH(1:10)) GO TO 20
      WRITE(38,5001) CARDD
      GO TO 20
   90 CONTINUE
      REWIND 37
      GO TO 10
  100 CONTINUE
      RETURN
 5000 FORMAT(A98)
 5001 FORMAT(A78)
 5002 FORMAT(A80)
      END
