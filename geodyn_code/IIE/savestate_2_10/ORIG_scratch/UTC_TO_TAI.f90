!UTC_TO_TAI
      SUBROUTINE UTC_TO_TAI(MJD,UTC,TAI)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
      SAVE

      DIMENSION MJD_LPS(26),UTC_M_TAI(26)
      DATA MJD_LPS/41317, &
     &             41499, &
     &             41683, &
     &             42048, &
     &             42413, &
     &             42778, &
     &             43144, &
     &             43509, &
     &             43874, &
     &             44239, &
     &             44786, &
     &             45151, &
     &             45516, &
     &             46247, &
     &             47161, &
     &             47892, &
     &             48257, &
     &             48804, &
     &             49169, &
     &             49534, &
     &             50083, &
     &             50630, &
     &             51179, &
     &             53736, &
     &             54832, &
     &             56109   /
      DATA UTC_M_TAI/-10.0,-11.0,-12.0,-13.0,-14.0,-15.0,-16.0,-17.0, &
     &               -18.0,-19.0,-20.0,-21.0,-22.0,-23.0,-24.0,-25.0, &
     &               -26.0,-27.0,-28.0,-29.0,-30.0,-31.0,-32.0,-33.0, &
     &               -34.0,-35.0 /
!******************************************************************************
!  BEGIN EXECUTABLE CODE
!******************************************************************************
      DO 410 J1=1,26
      IF ( MJD .GE. MJD_LPS(J1) ) THEN
      TAI = UTC - UTC_M_TAI(J1)
      ELSE
      GOTO 810
      END IF
 410  CONTINUE
 810  CONTINUE

      RETURN
      END  SUBROUTINE  UTC_TO_TAI
