      SUBROUTINE PDUMP1(NM,NP,PMPA,RESID)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
      DIMENSION PMPA(NP,NM),RESID(NM)
!
      WRITE(6,6000)
!
      DO I=1,NM
        WRITE(6,6001) I,RESID(I),PMPA(1,I)
      ENDDO
!
      RETURN
 6000 FORMAT(' PDUMP1 ENTERED')
 6001 FORMAT(' I,RESID,PMPA(1) ',I5,2D20.11)
      END
