!    PATTERN: S1-->T1
      IF(.NOT.LLOCRI) GO TO 2600
! BINRES FOR 1ST STATION & SATELLITE
      CALL INRTSV(COSTHG(1,1),SINTHG(1,1),BRTS,BRTSV,NM,MINTIM)
!
! WRITE OUT ALL STATION AND ALL SATELLITE
      CALL UTCET(.FALSE.,NM,MJDSN,FSECN,AA(KCFSC),AA(KA1UT))
!
!     ....STATION COORDINATES RECORD ON BINARY RESIDUAL FILE
!
!
! -----< NAO MODIFICATION (Not done yet) >-----
! (these output parts are not modified to handle interplanetary
!  measurements because of the lack of time, the modification
!  should eventually be needed K. Heki)
! -----< /NAO MODIFICATION (Not done yet) >-----
!
      WRITE(IUNT19) (AA(KCFSC-1+JP),JP=1,NM),(COSTHG(JP,1),JP=1,NM),    &
     &              (SINTHG(JP,1),JP=1,NM),(BRTS(JP,1),JP=1,NM),        &
     &              (BRTS(JP,2),JP=1,NM), (BRTS(JP,3),JP=1,NM),         &
     &              (BRTSV(JP,1),JP=1,NM),(BRTSV(JP,2),JP=1,NM),        &
     &              (BRTSV(JP,3),JP=1,NM)
!
      IF(IEXIT.NE.1) GO TO 2600
      CALL UTCET(.FALSE.,NM,MJDSN,FSECM,AA(KCFSC),AA(KA1UT))
      IF(LOBORB) GO TO 2550
      CALL SATUPD(MJDSN,FSECM,XSM,AA(KTRJX),AA,NM,MINTIM,.TRUE.,.FALSE.,&
     &II,0,1)
      CALL SATUPD(MJDSN,FSECM,VSM,AA(KTRJV),AA,NM,MINTIM,.FALSE.,       &
     &           .FALSE.,II,0,1)
 2550 CONTINUE
      WRITE(IUNT19)   (AA(KCFSC-1+JP),JP=1,NM),(AA(KTRJX-1+JP),JP=1,NM),&
     &(AA(KTRJX+MINTIM-1+JP),JP=1,NM),(AA(KTRJX+2*MINTIM-1+JP),JP=1,NM),&
     &(AA(KTRJV-1+JP),JP=1,NM),(AA(KTRJV+MINTIM-1+JP),JP=1,NM),         &
     &(AA(KTRJV+2*MINTIM-1+JP),JP=1,NM)
 2600 CONTINUE
      IF(IEXIT.EQ.1) RETURN
 2750 CONTINUE
! IF TRANSIT TIME CORRECTIONS ARE NOT REQUIRED SKIP TO 4000

      ISATID=II(KISATN+INDSAT(1)-1)
      IF(NINTOT.GT.0) THEN
      CALL FNDNUM(ISATID,II(KSATIN),NINTOT,IRET)
      IF(IRET.GT.0) THEN
      KSUMXOS=MAPSAT(INTOSP,1)
      KSUMPOS=MAPSAT(INTOSP,2)
      KXDDTOS=MAPSAT(INTOSP,3)
      KPDDTOS=MAPSAT(INTOSP,4)
      KNSTEPS=II(KSTEPS-1+IRET)
      NEQNI=II(KNEQNI-1+IRET)
      ENDIF
      ENDIF

      CALL ORBSET(II(KNSAT),II(KIVSAT),LL(KLSETS),INDSAT(2),            &
     &   KNSTEPS,NEQNI,II(KSGMNT-1+IRET),                               &
     &   AA(KSGTM1-1+IRET),AA(KSGTM2-1+IRET),IRET,MJDSN,FSECM(1),       &
     &   FSECM(NM),NM,AA,II)
      IF(LIGHT) GO TO 4000
!
!***** START HERE TO COMPUTE LINK #2 TRANSIT TIMES TO SATELLITE ********
!
! INITIALIZE SATELLITE Sk TIMES TO EQUAL SATELLITE Sm TIMES
!            TRANSIT TIME
      DO 3000 N=1,NM
      FSECK(N)=FSECM(N)
 3000 END DO
!
! -----< NAO MODIFICATION >-----
! XSM    -> XSS
! DELCTN -> DELCTI
! PLANX  -> SCRTM
! Add .TRUE. at the end of argument array.
! ITERATIVELY COMPUTE SATELLITE Sk TIMES

      ISATID=II(KISATN+INDSAT(1)-1)
!
      CALL LITEUP(XSMPC,XSK,XSKPC,RKM,URKM,URKM(1,2,1),URKM(1,3,1),S,   &
     &            MJDSN,FSECM,FSECK,SIGNL,DELCTI,RSUNST,GRELT2,PSTAT,NM,&
     &            SCRTM,AA(KTPMES+4*MINTIM),LL(KLTPMS+6*MINTIM),        &
     &            ISATID,AA,II,LL,.TRUE.)

! -----< END /NAO MODIFICATION >-----
!
!
!***** TRANSIT TIMES FOR LINK #2 COMPUTED ******************************
!
 4000 CONTINUE
!
      ISATID=II(KISATN+INDSAT(2)-1)
      IF(NINTOT.GT.0) THEN
      CALL FNDNUM(ISATID,II(KSATIN),NINTOT,IRET)
      IF(IRET.GT.0) THEN
      KSUMXOS=MAPSAT(INTOSP,1)
      KSUMPOS=MAPSAT(INTOSP,2)
      KXDDTOS=MAPSAT(INTOSP,3)
      KPDDTOS=MAPSAT(INTOSP,4)
      KNSTEPS=II(KSTEPS-1+IRET)
      NEQNI=II(KNEQNI-1+IRET)
      ENDIF
      ENDIF

! OBTAIN SATELLITE POSITIONS AT CORRECT SATELLITE TIMES
      CALL ORBIT(MJDSN,FSECK,AA(KS),NM,II(KINDH),II(KNMH),II(KMJDSC),   &
     &   AA(KFSEC),II(KMJDSV),AA(KFSECV),LL(KLSETS),II(KIPXPF),         &
     &   II(KIVSAT),1,II(KNMAX),II(KNTOLD),XSK,AA(KPXPFK),              &
     &   LNADJ,.FALSE.,II(KPLPTR),II(KPANEL),II(KNMOVE),                &
     &   AA(KTPMES+4*MINTIM),LL(KLTPMS+6*MINTIM),LL(KSETDN),            &
     &   AA(KTMOS0-1+INTOSP),AA(KTMOS-1+INTOSP),AA(KTMOSP-1+INTOSP),    &
     &   AA(KSMXOS-1+KSUMXOS),AA(KSMXOS-1+KSUMPOS),AA(KSMXOS-1+KXDDTOS),&
     &   AA(KSMXOS-1+KPDDTOS),KNSTEPS,NEQNI,AA(KSGTM1-1+IRET),          &
     &   AA(KSGTM1-1+IRET),AA(KSGTM2-1+IRET),AA,II,LL)

      IF(LVMTFO) CALL VMATO(AA(KA1UT),MJDSN,FSECK,XSK,VSK,AA(KPXPFK),   &
     &   II(KINDH),II(KNMH),AA(KS),AA(KCIP  ),NM,INDSAT(2),II)
      KPXPFJ=KPXPFK+II(KIPXPF+1)-II(KIPXPF)
      IF(.NOT.LOBORB.AND.LACC) GO TO 4100
      CALL TDORTR(MJDSN,FSECK,XSK,AA(KTRJX),AA,NM,MINTIM,.TRUE.,.FALSE.,&
     &II)
      CALL TDORTR(MJDSN,FSECK,VSK,AA(KTRJV),AA,NM,MINTIM,.FALSE.,       &
     &            .FALSE.,II)
! COMPUTE RIGHT ASCENSION OF GREENWICH ACROSS ENTIRE BLOCK OF DATA
      CALL GRHRAN(MJDSN,FSECK,.FALSE.,.FALSE.,AA(KEQN),AA(KSRTCH),      &
     & AA(KTHETG),COSTHG(1,2),SINTHG(1,2),NM,AA,II,UTDT(1,2),.FALSE.,   &
     & DUM,DUM1)
! SATELLITE TO SATELLITE SIMULATIONS VISIBILITY CHECKING
! COMPUTE RANGES BETWEEN SATELLITE M AND GEOCENTER
      IF(.NOT.LPSBL)GOTO 4090
      DO 4080 I=1,NM
      XSKBF(I,1)=XSK(I,1)*COSTHG(I,2)+XSK(I,2)*SINTHG(I,2)+XSK(I,3)*ZERO
      XSKBF(I,2)=XSK(I,1)*(-SINTHG(I,2))+                               &
     &                                XSK(I,2)*COSTHG(I,2)+XSK(I,3)*ZERO
      XSKBF(I,3)=XSK(I,1)*ZERO+XSK(I,2)*ZERO+XSK(I,3)*ONE
 4080 END DO
 4090 CONTINUE
!
! COMPUTE S/C HEIGHT ABOVE ELLIPSOID AND SUB-SATELLITE LAT & LON
      CALL SUBTRK(AA(KTRJX),AA(KTHETG),AA(KXY2),AA(KZT),AA(KRT),        &
     &   AA(KSATLT),AA(KSATLN),AA(KSATH),NM)
!
! -----< NAO MODIFICATION >-----
! Commented out by K. Matsumoto 2000.01.28
! PRINT TRAJECTORY
!      CALL TRAJCT(AA(KA1UT),MJDSN,FSECK,AA(KTRJX),AA(KTRJV),            &
!     &   AA(KSATLT),AA(KSATLN),AA(KSATH),II(KINDH),II(KNMH),AA(KS),     &
!     &   AA(KCIP  ),NM,INDSAT(2),AA,AA(KCOSTH),AA(KSINTH),AA(KDPSR),    &
!     &   AA(KXPUT),AA(KYPUT),II)
! -----< /END NAO MODIFICATION >-----
!
 4100 CONTINUE
        DO 4125 I=1,3
        DO 4125 J=1,MINTIM
           XSKPC(J,I)=XSK(J,I)
 4125   CONTINUE
      IF(ITBDGM.NE.ICBDGM) THEN
!
! GET SAT BARYCENTRIC
! FIRST  SAVE OFF PLANET CENTERED COORDINATES
      CALL BARYC(MJDSN,FSECK,XSK,VSK,MINTIM,IGRP,IORCB,IORMO,IORSUN,    &
     &           IOREM,COEF,COEFMO,COEFS,COEFEM,                        &
     &           NM,NDIM1,NDIM2,.FALSE.,.FALSE.,.FALSE.,AA(KPRCBD),     &
     &           AA(KPRTBD),SCRTM(1,1),SCRTM(1,7),SCRTM(1,10),          &
     &           CHCBS,SCRTM(1,16),SCRTM(1,31),SCRTM(1,46),SCRTM(1,31), &
     &           SCRTM(1,46),SCRTM(1,74),SCRTM(1,75),SCRTM(1,76),AA,II)
!
      ENDIF
!
!***** START HERE TO COMPUTE LINK #2 MEASUREMENT ***********************
!
! COMPUTE RANGES AND UNIT VECTORS FROM SATELLITE Sk TO SATELLITE Sm
      CALL RANGE(XSM,XSK,URKM(1,1,2),RKM,S,NM,.TRUE.)
      IF(LSIMDT) CALL OCCUSS(XSMPC,XSKPC,LOCCLT,NM,MINTIM)
!
! FOR SAT-SAT-STA. MEASUREMENT CALCULATE PERIODIC RELATIVISTIC
! CORRECTION IF NESS. APPLICATION OF CORRECTION IS DONE IN ROBSUM.
      IF(LITER1) THEN
      IF(((MTYPE.EQ.55).OR.(MTYPE.EQ.56).OR.(MTYPE.EQ.65))              &
                      .AND.(IRLONA.GT.0)) THEN
        IF(NRLTMC.GT.0) THEN
          ISATID=II(KISATN+INDSAT(2)-1)
          JSATID=II(KISATN+INDSAT(3)-1) ! ONLY USED FOR MTYPE 65
          DO 4160 ISTID=1,NRLTMC
            IF(ISATID.EQ.IDRLTM(ISTID)) THEN
              ITRL=ITRLTM(ISTID)
              GOTO 4170
            ENDIF
            IF (MTYPE.EQ.65) THEN
                IF(JSATID.EQ.IDRLTM(ISTID)) THEN
                  ITRL=ITRLTM(ISTID)
                  GOTO 4170
                ENDIF
            END IF
 4160     CONTINUE
          GOTO 4190
        ELSE
          ITRL=IRLONA-1
        ENDIF
 4170   CONTINUE
! SET LRLCLK TO .TRUE. IN ORDER TO APPLY CORRECTION IN ROBSUM
        LRLCLK=.TRUE.
        CALL RELTMC(XSK,XTN,VSK,MJDSN,FSECK,NM,AA(KRLCOR),              &
     &   ITRL,LRRATE,LADD)
      ENDIF
      ENDIF
 4190 CONTINUE
!
! SATELLITE TO SATELLITE SIMULATIONS VISIBILITY CHECKING
      IF (.NOT.LPSBL) GOTO 4490
! RKM IS THE RANGE BETWEEN SATELLITE 1 AND SATELLITE 2
! RSSV1 IS THE RANGE FROM SATELLITE K TO THE GEOCENTER
! RSSV2 IS THE RANGE FROM SATELLITE M TO THE GEOCENTER
! ERSQ IS THE SQUARE OF THE EARTH'S RADIUS
      DO 4480 I=1,NM
      RSSV1(I)=SQRT(XSMBF(I,1)**2+XSMBF(I,2)**2+XSMBF(I,3)**2)
      RSSV2(I)=SQRT(XSKBF(I,1)**2+XSKBF(I,2)**2+XSKBF(I,3)**2)
      RR1=RSSV1(I)**2
      RR2=RSSV2(I)**2
      RR1=SQRT(RR1-ERSQ)
      RR2=SQRT(RR2-ERSQ)
      RR=RR1+RR2
      IF(RKM(I).GE.RR) THEN
      LPTS(I)=.FALSE.
      ENDIF
 4480 END DO
 4490 CONTINUE
!
! COULD CONVERT UNIT VECTOR AT TIME M OR K (PROBABLY WON'T BE USED)
      CALL TDORTR(MJDSN,FSECM,URKM(1,1,2),URKM,AA,NM,NM,.TRUE.,.FALSE., &
     &II)
! IF RANGE RATES NOT REQUIRED, SKIP TO 4500
      IF(LRANGE) GO TO 4500
! COMPUTES RANGE RATES AND PARTIALS OF RANGE RATES W.R.T. SAT. POSITIONS
      CALL RNGRAT(VSM,VSK,URKM,RKM,RRKM,RELV,PRRPKM,NM,.TRUE.)
 4500 CONTINUE
      IF(.NOT.LLOCRI) GO TO 4900
! WRITE FIRST SATELLITE
      CALL UTCET(.FALSE.,NM,MJDSN,FSECM,AA(KCFSC),AA(KA1UT))
      IF(LOBORB) GO TO 4600
      CALL SATUPD(MJDSN,FSECM,XSM,AA(KTRJX),AA,NM,MINTIM,.TRUE.,.FALSE.,&
     &II,0,1)
      CALL SATUPD(MJDSN,FSECM,VSM,AA(KTRJV),AA,NM,MINTIM,.FALSE.,       &
     &            .FALSE.,II,0,1)
 4600 CONTINUE
      WRITE(IUNT19)   (AA(KCFSC-1+JP),JP=1,NM),(AA(KTRJX-1+JP),JP=1,NM),&
     &(AA(KTRJX+MINTIM-1+JP),JP=1,NM),(AA(KTRJX+2*MINTIM-1+JP),JP=1,NM),&
     &(AA(KTRJV-1+JP),JP=1,NM),(AA(KTRJV+MINTIM-1+JP),JP=1,NM),         &
     &(AA(KTRJV+2*MINTIM-1+JP),JP=1,NM)
! WRITE OUT SECOND SATELLITE
      CALL UTCET(.FALSE.,NM,MJDSN,FSECK,AA(KCFSC),AA(KA1UT))
      IF(LOBORB) GO TO 4700
      CALL SATUPD(MJDSN,FSECK,XSK,AA(KTRJX),AA,NM,MINTIM,.TRUE.,        &
     &.FALSE.,II,0,1)
      CALL SATUPD(MJDSN,FSECK,VSK,AA(KTRJV),AA,NM,MINTIM,.FALSE.,       &
     &            .FALSE.,II,0,1)
 4700 CONTINUE
      WRITE(IUNT19)   (AA(KCFSC-1+JP),JP=1,NM),(AA(KTRJX-1+JP),JP=1,NM),&
     &(AA(KTRJX+MINTIM-1+JP),JP=1,NM),(AA(KTRJX+2*MINTIM-1+JP),JP=1,NM),&
     &(AA(KTRJV-1+JP),JP=1,NM),(AA(KTRJV+MINTIM-1+JP),JP=1,NM),         &
     &(AA(KTRJV+2*MINTIM-1+JP),JP=1,NM)
 4900 CONTINUE
!
!***** LINK #2 MEASUREMENT COMPUTED ************************************
!
! IF 3-WAY SAT. TO SAT. TO STA. MEASUREMENT, COMPUTATIONS ARE COMPLETE.
!    PATTERN: Sk-->Sm-->Tn
      IF(IEXIT.EQ.2) RETURN
! IF TRANSIT TIME CORRECTIONS ARE NOT REQUIRED SKIP TO 6000

      ISATID=II(KISATN+INDSAT(1)-1)
      IF(NINTOT.GT.0) THEN
      CALL FNDNUM(ISATID,II(KSATIN),NINTOT,IRET)
      IF(IRET.GT.0) THEN
      KSUMXOS=MAPSAT(INTOSP,1)
      KSUMPOS=MAPSAT(INTOSP,2)
      KXDDTOS=MAPSAT(INTOSP,3)
      KPDDTOS=MAPSAT(INTOSP,4)
      KNSTEPS=II(KSTEPS-1+IRET)
      NEQNI=II(KNEQNI-1+IRET)
      ENDIF
      ENDIF

      CALL ORBSET(II(KNSAT),II(KIVSAT),LL(KLSETS),INDSAT(3),            &
     &   KNSTEPS,NEQNI,II(KSGMNT-1+IRET),                               &
     &   AA(KSGTM1-1+IRET),AA(KSGTM2-1+IRET),IRET,MJDSN,FSECJ(1),       &
     &   FSECJ(NM),NM,AA,II)
      IF(LIGHT) GO TO 6000
!
!***** START HERE TO COMPUTE LINK #3 TRANSIT TIMES TO SATELLITE ********
!
! INITIALIZE SATELLITE Sj TIMES TO EQUAL SATELLITE Sk TIMES
!            TRANSIT TIME
      DO 5000 N=1,NM
      FSECJ(N)=FSECK(N)
 5000 END DO
!
      ISATID=II(KISATN+INDSAT(1)-1)
!
      CALL LITEUP(XSKPC,XSJ,XSJPC,RKJ,URKJ,URKJ(1,2,1),URKJ(1,3,1),S,   &
     &            MJDSN,FSECK,FSECJ,SIGNL,DELCTI,RSUNST,GRELT3,PSTAT,NM,&
     &            SCRTM,AA(KTPMES+6*MINTIM),LL(KLTPMS+9*MINTIM),        &
     &            ISATID,AA,II,LL,.TRUE.)
!
!***** TRANSIT TIMES FOR LINK #3 COMPUTED ******************************
!
 6000 CONTINUE

      ISATID=II(KISATN+INDSAT(1)-1)
      IF(NINTOT.GT.0) THEN
      CALL FNDNUM(ISATID,II(KSATIN),NINTOT,IRET)
      IF(IRET.GT.0) THEN
      KSUMXOS=MAPSAT(INTOSP,1)
      KSUMPOS=MAPSAT(INTOSP,2)
      KXDDTOS=MAPSAT(INTOSP,3)
      KPDDTOS=MAPSAT(INTOSP,4)
      KNSTEPS=II(KSTEPS-1+IRET)
      NEQNI=II(KNEQNI-1+IRET)
      ENDIF
      ENDIF

! OBTAIN SATELLITE POSITIONS AT CORRECT SATELLITE TIMES
      CALL ORBIT(MJDSN,FSECJ,AA(KS),NM,II(KINDH),II(KNMH),II(KMJDSC),   &
     &   AA(KFSEC),II(KMJDSV),AA(KFSECV),LL(KLSETS),II(KIPXPF),         &
     &   II(KIVSAT),1,II(KNMAX),II(KNTOLD),XSJ,AA(KPXPFJ),              &
     &   LNADJ,.FALSE.,II(KPLPTR),II(KPANEL),II(KNMOVE),                &
     &   AA(KTPMES+6*MINTIM),LL(KLTPMS+9*MINTIM),LL(KSETDN),            &
     &   AA(KTMOS0-1+INTOSP),AA(KTMOS-1+INTOSP),AA(KTMOSP-1+INTOSP),    &
     &   AA(KSMXOS-1+KSUMXOS),AA(KSMXOS-1+KSUMPOS),AA(KSMXOS-1+KXDDTOS),&
     &   AA(KSMXOS-1+KPDDTOS),KNSTEPS,NEQNI,AA(KSGTM1-1+IRET),          &
     &   AA(KSGTM1-1+IRET),AA(KSGTM2-1+IRET),AA,II,LL)

      IF(LVMTFO) CALL VMATO(AA(KA1UT),MJDSN,FSECJ,XSJ,VSJ,AA(KPXPFJ),   &
     &   II(KINDH),II(KNMH),AA(KS),AA(KCIP  ),NM,INDSAT(3),II)
      IF(.NOT.LOBORB) GO TO 6100
      CALL TDORTR(MJDSN,FSECJ,XSJ,AA(KTRJX),AA,NM,MINTIM,.TRUE.,.FALSE.,&
     &II)
      CALL TDORTR(MJDSN,FSECJ,VSJ,AA(KTRJV),AA,NM,MINTIM,.FALSE.,       &
     &            .FALSE.,II)
! COMPUTE RIGHT ASCENSION OF GREENWICH ACROSS ENTIRE BLOCK OF DATA
      CALL GRHRAN(MJDSN,FSECJ,.FALSE.,.FALSE.,AA(KEQN),AA(KSRTCH),      &
     & AA(KTHETG),COSTHG(1,2),SINTHG(1,2),NM,AA,II,UTDT(1,2),.FALSE.,   &
     & DUM,DUM1)
! COMPUTE S/C HEIGHT ABOVE ELLIPSOID AND SUB-SATELLITE LAT & LON
      CALL SUBTRK(AA(KTRJX),AA(KTHETG),AA(KXY2),AA(KZT),AA(KRT),        &
     &   AA(KSATLT),AA(KSATLN),AA(KSATH),NM)
!
! -----< NAO MODIFICATION >-----
! Commented out by K. Matsumoto 2000.01.28
! PRINT TRAJECTORY
!      CALL TRAJCT(AA(KA1UT),MJDSN,FSECJ,AA(KTRJX),AA(KTRJV),            &
!     &   AA(KSATLT),AA(KSATLN),AA(KSATH),II(KINDH),II(KNMH),AA(KS),     &
!     &   AA(KCIP  ),NM,INDSAT(3),AA,AA(KCOSTH),AA(KSINTH),AA(KDPSR),    &
!     &   AA(KXPUT),AA(KYPUT),II)
! -----< /NAO MODIFICATION >-----
!
 6100 CONTINUE
        DO I=1,NM
          XSJPC(I,1)=XSJ(I,1)
          XSJPC(I,2)=XSJ(I,2)
          XSJPC(I,3)=XSJ(I,3)
        ENDDO
      IF(ITBDGM.NE.ICBDGM) THEN
!
! GET SAT BARYCENTRIC
!
      CALL BARYC(MJDSN,FSECJ,XSJ,VSJ,MINTIM,IGRP,IORCB,IORMO,IORSUN,    &
     &           IOREM,COEF,COEFMO,COEFS,COEFEM,                        &
     &           NM,NDIM1,NDIM2,.FALSE.,.FALSE.,.TRUE.,AA(KPRCBD),      &
     &           AA(KPRTBD),SCRTM(1,1),SCRTM(1,7),SCRTM(1,10),          &
     &           CHCBS,SCRTM(1,16),SCRTM(1,31),SCRTM(1,46),SCRTM(1,31), &
     &           SCRTM(1,46),SCRTM(1,74),SCRTM(1,75),SCRTM(1,76),AA,II)
      ENDIF
      DO I=1,NM
        RSUNST(I,2)=SQRT(SCRTM(I,7)*SCRTM(I,7)+SCRTM(I,8)*SCRTM(I,8)   &
     &                   +SCRTM(I,9)*SCRTM(I,9))
      ENDDO
!
!
!***** START HERE TO COMPUTE LINK #3 MEASUREMENT ***********************
!
! COMPUTE RANGES AND UNIT VECTORS FROM SATELLITE Sk TO SATELLITE Sj
      CALL RANGE(XSJ,XSK,URKJ(1,1,2),RKJ,S,NM,.TRUE.)
      IF(LSIMDT) CALL OCCUSS(XSJPC,XSKPC,LOCCLT,NM,MINTIM)
! COULD CONVERT UNIT VECTOR AT TIME K OR J (PROBABLY WON'T BE USED)
      CALL TDORTR(MJDSN,FSECK,URKJ(1,1,2),URKJ,AA,NM,NM,.TRUE.,.FALSE., &
     &II)
! IF RANGE RATES NOT REQUIRED, SKIP TO 6500
      IF(LRANGE) GO TO 6500
! COMPUTES RANGE RATES AND PARTIALS OF RANGE RATES W.R.T. SAT. POSITIONS
      CALL RNGRAT(VSJ,VSK,URKJ,RKJ,RRKJ,RELV,PRRPKJ,NM,.TRUE.)
 6500 CONTINUE
      IF(.NOT.LLOCRI) GO TO 6900
! WRITE OUT THIRD SATELLITE
      CALL UTCET(.FALSE.,NM,MJDSN,FSECJ,AA(KCFSC),AA(KA1UT))
      IF(LOBORB) GO TO 6800
      CALL SATUPD(MJDSN,FSECJ,XSJ,AA(KTRJX),AA,NM,MINTIM,.TRUE.,        &
     &.FALSE.,II,0,1)
      CALL SATUPD(MJDSN,FSECJ,VSJ,AA(KTRJV),AA,NM,MINTIM,.FALSE.,       &
     &            .FALSE.,II,0,1)
 6800 CONTINUE
      WRITE(IUNT19)   (AA(KCFSC-1+JP),JP=1,NM),(AA(KTRJX-1+JP),JP=1,NM),&
     &(AA(KTRJX+MINTIM-1+JP),JP=1,NM),(AA(KTRJX+2*MINTIM-1+JP),JP=1,NM),&
     &(AA(KTRJV-1+JP),JP=1,NM),(AA(KTRJV+MINTIM-1+JP),JP=1,NM),         &
     &(AA(KTRJV+2*MINTIM-1+JP),JP=1,NM)
 6900 CONTINUE
!
!***** LINK #3 MEASUREMENT COMPUTED ************************************
!
! IF SAT. TO SAT. TO SAT. TO STA. MEAS., COMPUTATIONS ARE COMPLETE.
!    PATTERN: Sj-->Sk-->Sm-->Tn
! IF SAT. TO SAT. TO SAT. MEASUREMENT, COMPUTATIONS ARE COMPLETE.
!    PATTERN: Sj-->Sk-->Sm
      IF(IEXIT.EQ.3) RETURN
!
!***** START HERE TO COMPUTE INERTIAL TRACKING STATION #i POSITIONS ****
!
! IF TRANSIT TIME CORRECTIONS ARE NOT REQUIRED SKIP TO 8000
      IF(LIGHT) GO TO 8000
!
!***** START HERE TO COMPUTE LINK #4 TRANSIT TIMES TO STATION **********
!
! INITIALIZE STATION TIMES TO EQUAL SATELLITE Sj TIMES
      DO 7000 N=1,NM
      FSECI(N)=FSECJ(N)
 7000 END DO
!
! OBTAIN POLAR MOTION ROTATION MATRICES AND PARTIAL DERIVATIVE MATRICES
!        FROM WHICH TO INTERPOLATE ACROSS THE ENTIRE BLOCK OF DATA

      NINTP2=NINTPL(2)*2

      lyara = .false.
      isnumb = 0

       CALL STATPV(AA,II,LL,LDNPOL,LPOLAD,MJDSN,FSECI,NM,XMI,NINTPL(2),&
     &   NMP(1,2),INDP(1,2),PXPOLE,RPOLE,S,XDPOLE,XTP,PXSPXP(1,1,1,2)  &
     &   ,PXSPLV(1,1,1,2),NINTP2,.FALSE.,INDSTA(2),.FALSE.,            &
     &   LYARA,ISNUMB,PXSPXD(1,1,1,2),COSTHG(1:NM,2),SINTHG(1:NM,2),   &
     &   AA(KSRTCH),UTDT(1,2),S1,FSECN,AA(KDXDNU),LNUTAD)

      CALL GRHRAN(MJDSN,FSECI,.TRUE.,.TRUE. ,AA(KEQN),AA(KSRTCH),       &
     & SINTHG(1,2),COSTHG(1,2),SINTHG(1,2),NM,AA,II,UTDT(1,2),.FALSE.,  &
     & XMI,DUM1)


        CALL LITEDN(AA,XSJ,XSJPC,XTP,XTI,VTI,COSTHG(1,2),SINTHG(1,2),   &
     &        RIJ,URIJ,URIJ(1,2,1),URIJ(1,3,1),S,MJDSN,FSECN,FSECJ,     &
     &        FSECI,SIGNL,DELCTN,DELCTI,RSUNST(1,2),GRELT4,PSTAT,NM,    &
     &        SCRTM,II,UTDT(1,1))

!
!***** TRANSIT TIMES FOR LINK #4 COMPUTED ******************************
!
 8000 CONTINUE
!
      IF(LOLMD) THEN
        CALL OLDSET(2,INDSTA(2),NM,AA(KSTAIN),II(KIP0OL),II(KNDOLA),    &
     &              II(KKIOLA),LOLAJ,LOLMDS,                            &
     &              JABCOF,JPXSPA,JOLPAE,JOLPAN,JOLPAV,JXLOCV,          &
     &              JABCCM,JPXSP1,JOLPXC,JOLPYC,JOLPZC,                 &
     &              JABCEO,JPXSP2,JOLPXP,JOLPYP,JOLPUP,LALOAD,ISITA,    &
     &              XENV)
      ENDIF
      IF(LEOPTO.OR.LOLMDS.OR.LXYZCO) THEN
         CALL OLTIME(MJDSN,FSECI,NM,AA(KANGFC),AA(KSPEED),AA(KSCROL),   &
     &               AA(KSCROL+4*NTOLFR),II(KPTOLS),AA(KANGT),          &
     &               AA(KCONAM),AA(KTWRK1),AA(KGRDAN),AA(KUANG),        &
     &               AA(KFAMP),II(KJDN))
      ENDIF
      IF(LEOPTO) THEN
         CALL OLDEOP(MJDSN,FSECI,NM,AA(JABCOF),AA(KANGFC),AA(KSPEED),   &
     &              AA(JXLOCV),XTP,AA(KSCROL),AA(KROTMT),LOLAJ,         &
     &              AA(JPXSPA),NPV0OL(1,2),NPV0OL(2,2),NPV0OL(3,2),     &
     &              II(JOLPAE),II(JOLPAN),II(JOLPAV),AA(JABCCM),        &
     &              AA(JPXSP1),NYZ0OL(1,2),NYZ0OL(2,2),NYZ0OL(3,2),     &
     &              II(JOLPXC),II(JOLPYC),II(JOLPZC),AA(JABCEO),        &
     &              AA(JPXSP2),NEO0OL(1,2),NEO0OL(2,2),NEO0OL(3,2),     &
     &              II(JOLPXP),II(JOLPYP),II(JOLPUP),LOLMDS,LXYZCO,     &
     &              LEOPTO,XMN)
      ENDIF

! OBTAIN POLAR MOTION ROTATION MATRICES AND PARTIAL DERIVATIVE MATRICES
!        FROM WHICH TO INTERPOLATE ACROSS THE ENTIRE BLOCK OF DATA

      NINTP2=NINTPL(2)*2

      lyara = .false.
      isnumb = 0

      CALL STATPVP(AA,II,LL,LDNPOL,LPOLAD,MJDSN,FSECI,NM,XMI,NINTPL(2), &
     &   NMP(1,2),INDP(1,2),PXPOLE,RPOLE,S,XDPOLE,XTP,PXSPXP(1,1,1,2)   &
     &   ,PXSPLV(1,1,1,2),NINTP2,.TRUE.,INDSTA(2),.FALSE.,              &
     &   LYARA,ISNUMB,PXSPXD(1,1,1,2),COSTHG(1:NM,2),SINTHG(1:NM,2),    &
     &   AA(KSRTCH),UTDT(1,2),S1,FSECI,AA(KDXDNU),LNUTAD)

      CALL GRHRAP(MJDSN,FSECI,.TRUE.,.TRUE. ,AA(KEQN),AA(KSRTCH),       &
     & SINTHG(1,2),COSTHG(1,2),SINTHG(1,2),NM,AA,II,UTDT(1,2),.FALSE.,  &
     & XMI,DUM1)

! ROTATE TRUE POLE STATION COORDINATES TO TRUE OF DATE ACROSS ENTIRE
!        BLOCK OF DATA
      IF(.NOT.LOLMDS.AND..NOT.LXYZCO) GO TO 8050
      isnumb = 0
      CALL OLOAD(MJDSN,FSECI,NM,AA(JABCOF),AA(KANGFC),AA(KSPEED),       &
     &           AA(JXLOCV),XTP,AA(KSCROL),AA(KROTMT),LOLAJ,            &
     &           AA(JPXSPA),NPV0OL(1,2),NPV0OL(2,2),NPV0OL(3,2),        &
     &           II(JOLPAE),II(JOLPAN),II(JOLPAV),AA(JABCCM),           &
     &           AA(JPXSP1),NYZ0OL(1,2),NYZ0OL(2,2),NYZ0OL(3,2),        &
     &           II(JOLPXC),II(JOLPYC),II(JOLPZC),AA(JABCEO),           &
     &           AA(JPXSP2),NEO0OL(1,2),NEO0OL(2,2),NEO0OL(3,2),        &
     &           II(JOLPXP),II(JOLPYP),II(JOLPUP),LOLMDS,LXYZCO,LEOPTO, &
     &           isnumb,.FALSE.,NM,LALOAD,AA(KALCOF),II(KSITE),ISITA,   &
     &           XENV)
 8050 CONTINUE
      CALL INERTP(XTP,COSTHG(1,2),SINTHG(1,2),XTI,NM,MINTIM,NM)
            DO 8060 IS=1,3
            DO 8060 NN=1,NM
            BRTS(NN,IS)=XTI(NN,IS)
            XTIPC(NN,IS)=XTI(NN,IS)
 8060       CONTINUE
      IF(.NOT.LRANGE)                                                   &
     &CALL INRTSV(COSTHG(1,2),SINTHG(1,2),XTI,VTI,NM,MINTIM)
      CALL TDORTR(MJDSN,FSECI,XTI,XTI,AA,NM,MINTIM,.TRUE.,.TRUE.,II)
      IF(ICBDGM.EQ.ITBDGM) GO TO 8099
!
!  PUT PLANET CENTERED COORDINATES INTO BARYCENTRIC
! GET COORDINATE EPHEMERIS TIME AT TRACKING STATION TIME
      DO 8070 N=1,NM
      RIJ(N)=FSECI(N)+DELCTI(N)
 8070 CONTINUE
! GET TRACKING BODY BARYCENTRIC
!
      CALL BARYC(MJDSN,RIJ,XTI,VTI,MINTIM,IGRP,IORTB,IORMO,IORSUN,      &
     &           IOREM,COEFEM,COEFMO,COEFS,COEFEM,                      &
     &           NM,NDIM1,NDIM2,.FALSE.,.TRUE.,.FALSE.,AA(KPRCBD),      &
     &           AA(KPRTBD),SCRTM(1,1),SCRTM(1,7),SCRTM(1,10),          &
     &           CHEMT,SCRTM(1,16),SCRTM(1,31),SCRTM(1,46),CHMOT,       &
     &           SCRTM(1,16),SCRTM(1,74),SCRTM(1,75),SCRTM(1,76),AA,II)
!
!   END INTERPLANETARY
!
 8099 CONTINUE
!
!***** INERTIAL TRACKING STATION #i POSITIONS NOW COMPUTED *************
!
!
!***** START HERE TO COMPUTE LINK #4 MEASUREMENT ***********************
!
!
! COMPUTE RANGES AND UNIT VECTORS FROM STATION TO SATELLITE
      CALL RANGE(XSJ,XTI,URIJ(1,1,2),RIJ,S,NM,.TRUE.)
!
      IF(ITBDGM.NE.ICBDGM) THEN
      IF(LSIMDT) CALL OCCULT(MJDSN,FSECM,NM,AA(KAESHD),XSJPC,XSJ,       &
     &                       URIJ(1,1,2),RSUNST(1,2),LOCCLT,LMSHD,      &
     &                       .FALSE.,PSTAT,AA,II)
      ENDIF
!
! GET TOD UNIT VECTOR AT SATION TIME
      CALL TDORTR(MJDSN,FSECI,URIJ(1,1,2),URIJ,AA,NM,NM,.FALSE.,        &
     &           .FALSE.,II)
! IF RANGE RATES NOT REQUIRED, RETURN
      IF(LRANGE) GO TO 8100
! ROTATE INERTIAL STATION VELOCITIES
      CALL TDORTR(MJDSN,FSECI,VTI,VTI,AA,NM,MINTIM,.FALSE.,.TRUE.,II)
! COMPUTES RANGE RATES AND PARTIALS OF RANGE RATES W.R.T. SAT. POSITIONS
      CALL RNGRAT(VSJ,VTI,URIJ,RIJ,RRIJ,RELV,PRRPIJ,NM,.TRUE.)
 8100 CONTINUE
      IF(.NOT.LLOCRI) RETURN
      CALL INRTSV(COSTHG(1,1),SINTHG(1,1),BRTS,BRTSV,NM,MINTIM)
!
! WRITE OUT FIRST STATION AND FIRST SATELLITE
      CALL UTCET(.FALSE.,NM,MJDSN,FSECI,AA(KCFSC),AA(KA1UT))
!
!     ....STATION COORDINATES RECORD ON BINARY RESIDUAL FILE
!
      WRITE(IUNT19) (AA(KCFSC-1+JP),JP=1,NM),(COSTHG(JP,1),JP=1,NM),    &
     &              (SINTHG(JP,1),JP=1,NM),(BRTS(JP,1),JP=1,NM),        &
     &              (BRTS(JP,2),JP=1,NM), (BRTS(JP,3),JP=1,NM),         &
     &              (BRTSV(JP,1),JP=1,NM),(BRTSV(JP,2),JP=1,NM),        &
     &              (BRTSV(JP,3),JP=1,NM)
!
!
!***** LINK #4 MEASUREMENT COMPUTED ************************************
!
      RETURN
      END
