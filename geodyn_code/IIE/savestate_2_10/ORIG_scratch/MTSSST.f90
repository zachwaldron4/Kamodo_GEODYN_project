!   PRRPKM   O    A    PARTL. OF RNG. RATE W.R.T. Tk-->Sm VECTOR
!   PRRPKJ   O    A    PARTL. OF RNG. RATE W.R.T. Tk-->Sj VECTOR
!   PRRPIJ   O    A    PARTL. OF RNG. RATE W.R.T. Ti-->Sj VECTOR
!   NMP      O    A    INDEX OF THE LAST MEASUREMENT ASSOCIATED WITH
