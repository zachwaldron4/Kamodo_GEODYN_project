!$IDANT
      SUBROUTINE IDANT(ISATID,ID2,LSAT2,IANT)
!********1*********2*********3*********4*********5*********6*********7**
! IDANT             00/00/00            0000.0    PGMR -  DAVE ROWLANDS
!
! FUNCTION:  IDENTIFY ANTENNA INFORMATION FOR A
!            SATELLITE ON A PARTICULAR MEASUREMENT LINK
!
! I/O PARAMETERS:
!
!   NAME    I/O  A/S   DESCRIPTION OF PARAMETERS
!   ------  ---  ---   ------------------------------------------------
!   ISATID   I    S    SATELLITE ID OF SATELLITE WHOSE ANTENNA NEEDS
!                      IDENTIFICATION
!   ID2      I    S    ID OF "OTHER OBJECT" (STATION OR SATELLITE)
!                      IN THE MEASUREMENT LINK
!   LSAT2    I    S    TRUE IF "OTHER OBJECT" IS A SATELLLITE
!   IANT     O    S    ANTENNA NUMBER TO BE USED
!
! COMMENTS:  IF IANT IS ZERO ON RETURN, THEN NO POSITIVE ID COULD
!            BE MADE FROM THE DATA RECORDS
!
!********1*********2*********3*********4*********5*********6*********7**
      IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
      SAVE
      COMMON/ANTID/ISATAN(3),ISTAN1(3),ISATN1(3),ISTAN2(3),ISATN2(3),   &
     &             IANNB1(3),IANNB2(3)
      COMMON/LANTID/LANTBD,LASALT
!
      IANT=0

! LANTBD -  IF TRUE, ANTENNA INFORMATION IS TRUE ON
!                    THE PREPRO WORDS OF THE BLOCK HEADER RECORDS

      IF(.NOT.LANTBD) RETURN
!
!  FIND OUT IF PRIMARY SATELLITE IS #1, #2 OR #3
      DO 100 ISAT=1,3
      IF(ISATID.EQ.ISATAN(ISAT)) GO TO 150
  100 END DO
      RETURN
  150 CONTINUE
!
!  FIND OUT WHICH NEIHBOR THE OTHER OBJECT IS
!
      IF(.NOT.LSAT2.AND.ID2.EQ.ISTAN1(ISAT)) THEN
         INB=1
         GO TO 500
      ENDIF
      IF(.NOT.LSAT2.AND.ID2.EQ.ISTAN2(ISAT)) THEN
         INB=2
         GO TO 500
      ENDIF
      IF(LSAT2.AND.ID2.EQ.ISATN1(ISAT)) THEN
         INB=1
         GO TO 500
      ENDIF
      IF(LSAT2.AND.ID2.EQ.ISATN2(ISAT)) THEN
         INB=2
         GO TO 500
      ENDIF
      RETURN
  500 CONTINUE
!
!  MAKE FINAL ID
!
      IF(INB.EQ.1) THEN
         IANT=IANNB1(ISAT)
      ELSE
         IANT=IANNB2(ISAT)
      ENDIF
      RETURN
      END
