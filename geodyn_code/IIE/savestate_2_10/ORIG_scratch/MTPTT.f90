!   VTK      O    A    TRUE OF DATE INERT. VEL. OF TRACKING STA. Tk
!   XMN      I    A    MEAN POLE COORDINATES OF TRACKING STATION Tn
!   XMK      I    A    MEAN POLE COORDINATES OF TRACKING STATION Tk
!   XMM      I    A    PLANETARY COORDINATES OF TARGET STATION PmTm
!   RNM      O    A    SLANT RANGE FROM Tn TO PmTm
!   RKM      O    A    SLANT RANGE FROM Tk TO PmTm
!   RRNM     O    A    RANGE RATE BETWEEN Tn AND PmTm
!   RRKM     O    A    RANGE RATE BETWEEN Tk AND PmTm
!   URNM     O    A    UNIT VECTOR FROM Tn TO PmTm
