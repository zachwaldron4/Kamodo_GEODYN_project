
        SUBROUTINE GETTMI(NM,FSEC,RNM,RKM,VLIGHT,ISEQ,TRINT,NDIMT)
        IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
        DIMENSION FSEC(NM),RNM(NM),RKM(NM),TRINT(NDIMT,2,2)
!
        DO 40110 I=1,NM
        TRINT(I,2,ISEQ)=FSEC(I)
        TRINT(I,1,ISEQ)=FSEC(I)-(RNM(I)+RKM(I))/VLIGHT
40110   CONTINUE
        RETURN
      END
