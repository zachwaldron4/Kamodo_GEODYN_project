      SUBROUTINE U63(N1,N2,NM,PMPA,RESID,WT,LNPNM)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
      DIMENSION PMPA(N1,N2),RESID(NM),WT(NM)
!
      IF(LNPNM) THEN
      DO 100 I=1,N2
      IF(WT(I).LT..000001D0) GO TO 100
      CALL DUMP(N1,RESID(I),WT(I),PMPA(1,I))
  100 END DO
      ELSE
      DO 200 I=1,N2
      CALL DUMP1(N1,N2,RESID(I),WT(I),PMPA,I)
      IF(WT(I).LT..000001D0) GO TO 200
  200 END DO
      ENDIF
      RETURN
      END
