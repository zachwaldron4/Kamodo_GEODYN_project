      SUBROUTINE SUMIT(NP,NM,ISP,ILP,NDATPA,PMPA,WT,ATPA)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
      DIMENSION PMPA(NP,NM),ATPA(NDATPA),WT(NM)
!
      NROWO=ILP-ISP+1
      NPP=NP-ISP+1
!
      DO 1000 IOBS=1,NM
      ILHS=0
      DO 500 IP1=1,NROWO
      IL=1+NPP-IP1
      IRHS=ISP+IP1-2
      PQ=WT(IOBS)*PMPA(IRHS+1,IOBS)
      DO 100 I=1,IL
      ATPA(ILHS+I)=ATPA(ILHS+I)+PQ*PMPA(IRHS+I,IOBS)
  100 END DO
      ILHS=ILHS+IL
  500 END DO
 1000 END DO
      RETURN
      END
