      SUBROUTINE GETSNM
      IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
! REFMT GOES FROM TRUE OF REF TO J2000 EEE
      COMMON/CREFMT/REFMT(9)
! SUMAT GOES FROM TRUE OF REF TO SUN 2000
      COMMON/SUNMT/SUNMAT(9)
      DIMENSION FSEC(1),RAPS(1),DCPS(1)
      DIMENSION SCRTCH(4),RPN1(1),RPN2(1),RPN3(1)
      DIMENSION RPN4(1),RPN5(1),RPN6(1),RPN7(1),RPN8(1),RPN9(1)
!
      DATA RAP/286.13D0/,DCP/63.87D0/
      DATA MJDSC/0/,FSEC/0.D0/
!
      RAPS(1)=RAP*ACOS(-1.D0)/180.D0
      DCPS(1)=DCP*ACOS(-1.D0)/180.D0
!
! RPN GOES FROM J2000 EEE TO SUN 2000

      CALL DXQN(MJDSC,FSEC,RAPS,DCPS,RPN1,RPN2,RPN3,RPN4,               &
     &                RPN5,RPN6,RPN7,RPN8,RPN9,SCRTCH,1)
!
! TO GET SUMAT, MULTIPLY RPN*REFMT
!
      SUNMAT(1)=RPN1(1)*REFMT(1)+RPN4(1)*REFMT(2)+RPN7(1)*REFMT(3)
      SUNMAT(2)=RPN2(1)*REFMT(1)+RPN5(1)*REFMT(2)+RPN8(1)*REFMT(3)
      SUNMAT(3)=RPN3(1)*REFMT(1)+RPN6(1)*REFMT(2)+RPN9(1)*REFMT(3)
!
      SUNMAT(4)=RPN1(1)*REFMT(4)+RPN4(1)*REFMT(5)+RPN7(1)*REFMT(6)
      SUNMAT(5)=RPN2(1)*REFMT(4)+RPN5(1)*REFMT(5)+RPN8(1)*REFMT(6)
      SUNMAT(6)=RPN3(1)*REFMT(4)+RPN6(1)*REFMT(5)+RPN9(1)*REFMT(6)
!
      SUNMAT(7)=RPN1(1)*REFMT(7)+RPN4(1)*REFMT(8)+RPN7(1)*REFMT(9)
      SUNMAT(8)=RPN2(1)*REFMT(7)+RPN5(1)*REFMT(8)+RPN8(1)*REFMT(9)
      SUNMAT(9)=RPN3(1)*REFMT(7)+RPN6(1)*REFMT(8)+RPN9(1)*REFMT(9)
!
      RETURN
      END
