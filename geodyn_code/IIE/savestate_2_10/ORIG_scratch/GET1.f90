      SUBROUTINE GET1(T,FSEC,NM,NMF,I1PT)
      IMPLICIT REAL*8(A-H,O-Z),LOGICAL(L)
      DIMENSION FSEC(NM)
!
! FIND THE POINT IN FSEC CLOSEST TO TIME T 
! THAT IS THE MIDDLE POINT
!
      DIST=1.D12
      DO I=1,NM
       TEST=DABS(FSEC(I)-T)
       IF(TEST.LT.DIST) THEN
         DIST=TEST
         I1PT=I
       ENDIF
      ENDDO
! GET 1ST POINT 
      NLEFT=NMF/2
      I1PT=I1PT-NLEFT
      RETURN
      END
