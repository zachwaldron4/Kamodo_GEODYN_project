!$SOLVA
      SUBROUTINE SOLVA(IAPARM,MAPARM,SUM1,SUM2,PDELTA)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
      COMMON/BIAOR/NAMBB,NCYCB,NAMBBH
      DIMENSION SUM1(1),SUM2(MAPARM),PDELTA(MAPARM)
!
      NUM=IAPARM-NAMBB
      DO 200 I=1,NAMBB
      PDELTA(I)=SUM2(I)
      IPOS=1+(I-1)*(MAPARM-NAMBB+1)
      DO 100 J=1,NUM
      PDELTA(I)=PDELTA(I)-SUM1(IPOS+J)*PDELTA(NAMBB+J)
  100 END DO
  200 END DO
      RETURN
      END
