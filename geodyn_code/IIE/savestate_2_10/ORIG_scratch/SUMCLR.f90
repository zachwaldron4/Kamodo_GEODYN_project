!$SUMCLR
      SUBROUTINE SUMCLR(AA,II,LL,LARC)
!********1*********2*********3*********4*********5*********6*********7**
! SUMCLR           00/00/00            0000.0    PGMR - ?
!
!
! FUNCTION:
!
! I/O PARAMETERS:
!
!   NAME    I/O  A/S   DESCRIPTION OF PARAMETERS
!   ------  ---  ---   ------------------------------------------------
!   AA
!   II
!   LL
!   LARC
!
!
! COMMENTS:
!
!
!********1*********2*********3*********4*********5*********6*********7**
      IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
      SAVE
      COMMON/CORA05/KOBTYP,KDSCRP,KGMEAN,KGLRMS,KWGMEA,KWGRMS,KTYMEA,   &
     &       KTYRMS,KWTMTY,KWTYRM,KTMEAN,KTRMS ,KWMEAN,KWTRMS,KWTRND,   &
     &       KPRVRT,KEBSTT,KVLOPT,NXCA05
      COMMON/CORI05/KNOBGL,KNOBWG,KNOBTY,KNOBWY,KNOBST,KNOBWT,KNEBOB,   &
     &              KJSTAT,NXCI05
      COMMON/CORL05/KLGPRV,NXCL05
      COMMON/CSTATA/RMSTOT,RMSEDT,RMSGLB
      COMMON/CSTATS/MSTATS,MTYPES,NXCSTT
      DIMENSION AA(1),II(1),LL(1)
      DATA ZERO/0.0D0/
!
!***********************************************************************
! START OF EXECUTABLE CODE *********************************************
!***********************************************************************
!
!***** DEBUG *****
!     PRINT 12345,MSTATS,MTYPES,LARC
!2345 FORMAT(' ** SUMCLR **  MSTATS,MTYPES,LARC=',2I12,1X,L1)
!     PRINT 12346,  KOBTYP,KDSCRP,
!    1              KGMEAN,KGLRMS,KWGMEA,KWGRMS,
!    2              KTYMEA,KTYRMS,KWTMTY,KWTYRM,
!    3              KTMEAN,KTRMS ,KWMEAN,KWTRMS,
!    4              KWTRND,KPRVRT
!2346 FORMAT(' ** SUMCLR ** CORA05=',2I12/(1X,4I12))
!     PRINT 12347,  KNOBGL,KNOBWG,KNOBTY,KNOBWY,KNOBST,KNOBWT
!2347 FORMAT(' ** SUMCLR ** CORI05=',2I12/(1X,4I12))
!     PRINT 12348,  KLGPRV
!2348 FORMAT(' ** SUMCLR ** CORL05=',I12)
!***** END DEBUG *****
!
      RMSTOT=ZERO
      RMSEDT=ZERO
!
      IF(.NOT.LARC) GO TO 5000
!
! THIS SET OF DYNAMIC ARRAYS CONTAINS TRACKING CONFIGURATION/MEASUREMENT
! TYPE SUMMATIONS AND IS TOTALLY ARC DEPENDENT AND IS CLEARED AT THE
! START OF EACH INNER ITERATION.
!
      CALL CLEARA(AA(KTMEAN),MSTATS)
      CALL CLEARA(AA(KTRMS ),MSTATS)
      CALL CLEARA(AA(KWMEAN),MSTATS)
      CALL CLEARA(AA(KWTRMS),MSTATS)
      CALL CLEARA(AA(KWTRND),MSTATS)
      CALL CLEARA(AA(KPRVRT),MSTATS)
      CALL CLEARI(II(KNOBST),MSTATS)
      CALL CLEARI(II(KNOBWT),MSTATS)
      CALL CLEARL(LL(KLGPRV),MSTATS)
!
! THIS SET OF DYNAMIC ARRAYS CONTAINS THE MEASUREMENT TYPE ONLY
! SUMMATIONS. IT ALSO IS TOTALLY ARC DEPENDENT AND IS CLEARED AT THE
! START OF EACH INNER ITERATION.
!
      CALL CLEARA(AA(KTYMEA),MTYPES)
      CALL CLEARA(AA(KTYRMS),MTYPES)
      CALL CLEARA(AA(KWTMTY),MTYPES)
      CALL CLEARA(AA(KWTYRM),MTYPES)
      CALL CLEARI(II(KNOBTY),MTYPES)
      CALL CLEARI(II(KNOBWY),MTYPES)
!
      RETURN
 5000 CONTINUE
!
! THIS SET OF DYNAMIC ARRAYS CONTAINS THE MEASUREMENT TYPE ONLY
! SUMMATIONS ACCUMULATED FROM THE LAST INNER ITERATIONS OF ALL ARCS.
! IT IS NOT ARC DEPENDENT AND IS CLEARED ONLY AT THE BEGINNING OF
! EACH GLOBAL ITERATION.
!
      CALL CLEARA(AA(KGMEAN),MTYPES)
      CALL CLEARA(AA(KGLRMS),MTYPES)
      CALL CLEARA(AA(KWGMEA),MTYPES)
      CALL CLEARA(AA(KWGRMS),MTYPES)
      CALL CLEARI(II(KNOBGL),MTYPES)
      CALL CLEARI(II(KNOBWG),MTYPES)
      RETURN
      END
