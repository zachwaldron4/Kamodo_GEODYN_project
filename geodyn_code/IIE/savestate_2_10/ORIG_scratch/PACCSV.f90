      SUBROUTINE PACCSV(NEQN,NSAT,NRAT,PXDDTH,PXDDOT,IACCP)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
      DIMENSION PXDDTH(NEQN,3,NSAT)
      DIMENSION PXDDOT(NEQN,3,NSAT,NRAT)
      DIMENSION IACCP(2,NSAT)
!
      NRAT1=NRAT-1
      DO 500 ISAT=1,NSAT
      I1=IACCP(1,ISAT)
      I2=IACCP(2,ISAT)
      NPARQ=I2-I1+1
      IF(IACCP(1,ISAT).EQ.0.OR.IACCP(2,ISAT).EQ.0) NPARQ=0
      IF(NPARQ.EQ.0) GO TO 500
!
      DO 300 ISTEP=1,NRAT1
      DO 100 IEQN=I1,I2
      PXDDTH(IEQN,1,ISAT)=PXDDTH(IEQN,1,ISAT)                           &
     &                   +PXDDOT(IEQN,1,ISAT,ISTEP)
      PXDDTH(IEQN,2,ISAT)=PXDDTH(IEQN,2,ISAT)                           &
     &                   +PXDDOT(IEQN,2,ISAT,ISTEP)
      PXDDTH(IEQN,3,ISAT)=PXDDTH(IEQN,3,ISAT)                           &
     &                   +PXDDOT(IEQN,3,ISAT,ISTEP)
  100 END DO
  300 END DO
      DO 400 IEQN=I1,I2
      PXDDTH(IEQN,1,ISAT)=PXDDTH(IEQN,1,ISAT)/DBLE(NRAT)
      PXDDTH(IEQN,2,ISAT)=PXDDTH(IEQN,2,ISAT)/DBLE(NRAT)
      PXDDTH(IEQN,3,ISAT)=PXDDTH(IEQN,3,ISAT)/DBLE(NRAT)
  400 END DO
  500 END DO
      RETURN
      END
