      DIMENSION ROTI(9)
      DIMENSION RA(1),DEC(1),SCRTCH(4)
      DIMENSION DPSI(1),EPST(1),EPSM(1)
      DIMENSION DUM(1)
      DIMENSION FSC(1)
      DIMENSION ACOSW(100),BCOSW(100)
      DIMENSION EQN(1),THTG(1),COSTHG(1),SINTHG(1)
!
!
      IF(LINTAX) THEN
          CALL GETOR(COSTG,SINTG,RA(1),DEC(1),2)
          CALL DXQN(MJDSEC,FSEC,RA,DEC,ROTI(1),ROTI(2),ROTI(3),         &
     &              ROTI(4),ROTI(5),ROTI(6),ROTI(7),ROTI(8),            &
     &              ROTI(9),SCRTCH,1)

          RETURN
      ENDIF
      FSC(1)=FSEC
      CALL PRXCSS(MJDSEC,FSC,RA,DEC,DUM,SCRTCH,1,DPSI,EPST,             &
     &           EPSM,AA,2)
      CALL DXQN(MJDSEC,FSEC,RA,DEC,ROTI(1),ROTI(2),ROTI(3),             &
     &          ROTI(4),ROTI(5),ROTI(6),ROTI(7),ROTI(8),                &
     &          ROTI(9),SCRTCH,1)
      CALL ROTXTR(MJDSEC,FSC,.FALSE.,EQN,SCRTCH,THTG,COSTHG,            &
     &                  SINTHG,1,AA,ACOSW,BSINW,AA(KANGWT),AA(KWT),     &
     &                  DUM,DUM,II,2)

      COSTG=COSTHG(1)
      SINTG=SINTHG(1)
      RETURN
      END
