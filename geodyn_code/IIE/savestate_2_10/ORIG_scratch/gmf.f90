!     parameter t
      t = SIN(dlat)

!     degree n and order m
      n = 9
      m = 9

! determine n!  (faktorielle)  moved by 1
      dfac(1) = 1
      do i = 1,(2*n + 1)
        dfac(i+1) = dfac(i)*i
      end do

!     determine Legendre functions (Heiskanen and Moritz, Physical Geodesy, 1967
      do i = 0,n
        do j = 0,MIN(i,m)
          ir = INT((i - j)/2)
          sum = 0
          do k = 0,ir
            sum = sum + (-1)**k*dfac(2*i - 2*k + 1)/dfac(k + 1)/        &
     &       dfac(i - k + 1)/dfac(i - j - 2*k + 1)*t**(i - j - 2*k)
          end do
!         Legendre functions moved by 1
          P(i + 1,j + 1) = 1.D0/2**i*SQRT((1 - t**2)**(j))*sum
        end do
      end do

!     spherical harmonics
      i = 0
      do n = 0,9
        do m = 0,n
          i = i + 1
          aP(i) = P(n+1,m+1)*COS(m*dlon)
          bP(i) = P(n+1,m+1)*SIN(m*dlon)
        end do
      end do

!     hydrostatic
      bh = 0.0029
      c0h = 0.062
      if (dlat.lt.0) then ! southern hemisphere
        phh  = pi
        c11h = 0.007
        c10h = 0.002
      else                ! northern hemisphere
        phh  = 0
        c11h = 0.005
        c10h = 0.001
      end if
      ch = c0h + ((COS(doy/365.25D0*2*pi + phh)+1)*c11h/2 + c10h)*    &
     &           (1-COS(dlat))

      ahm = 0.D0
      aha = 0.D0
      do i = 1,55
        ahm = ahm + (ah_mean(i)*aP(i) + bh_mean(i)*bP(i))*1D-5
        aha = aha + (ah_amp(i) *aP(i) + bh_amp(i) *bP(i))*1D-5
      end do
      ah  = ahm + aha*COS(doy/365.25D0*2.D0*pi)

      sine   = SIN(pi/2 - zd)
      beta   = bh/( sine + ch  )
      gamma  = ah/( sine + beta)
      topcon = (1.D0 + ah/(1.D0 + bh/(1.D0 + ch)))
      gmfh   = topcon/(sine+gamma)

!     height correction for hydrostatic mapping function from Niell (1996)
      a_ht = 2.53D-5
      b_ht = 5.49D-3
      c_ht = 1.14D-3
      hs_km  = dhgt/1000.D0
!
      beta   = b_ht/( sine + c_ht )
      gamma  = a_ht/( sine + beta)
      topcon = (1.D0 + a_ht/(1.D0 + b_ht/(1.D0 + c_ht)))
      ht_corr_coef = 1/sine - topcon/(sine + gamma)
      ht_corr      = ht_corr_coef * hs_km
      gmfh         = gmfh + ht_corr

!     wet
      bw = 0.00146
      cw = 0.04391

      awm = 0.D0
      awa = 0.D0
      do i = 1,55
        awm = awm + (aw_mean(i)*aP(i) + bw_mean(i)*bP(i))*1D-5
        awa = awa + (aw_amp(i) *aP(i) + bw_amp(i) *bP(i))*1D-5
      end do
      aw =  awm + awa*COS(doy/365.25D0*2*pi)

      beta   = bw/( sine + cw )
      gamma  = aw/( sine + beta)
      topcon = (1.D0 + aw/(1.D0 + bw/(1.D0 + cw)))
      gmfw   = topcon/(sine+gamma)

      end subroutine
