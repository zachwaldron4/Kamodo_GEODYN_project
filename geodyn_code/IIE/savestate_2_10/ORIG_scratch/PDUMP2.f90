      SUBROUTINE PDUMP2(NM,NP,PMPA,RESID)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
      DIMENSION PMPA(NM,NP),RESID(NM)
!
      WRITE(6,6000)
!
      DO I=1,NM
        WRITE(6,6001) I,RESID(I),PMPA(I,1)
      ENDDO
!
      RETURN
 6000 FORMAT(' PDUMP2 ENTERED')
 6001 FORMAT(' I,RESID,PMPA(1) ',I5,2D20.11)
      END
