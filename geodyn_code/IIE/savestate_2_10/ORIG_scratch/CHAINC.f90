      SUBROUTINE CHAINC(DCDP,PXEPA,ACOEF,NDM1,NDM2,ND1,ND2AC,NADJST,NM, &
     &                  MAXD,MADJST,IAVP,NAVP)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z),LOGICAL(L)
      COMMON/CESTML/LNPNM ,NXCSTL
      DIMENSION DCDP(MAXD,MADJST,3),PXEPA(NDM1,NDM2,3)
      DIMENSION ACOEF(MAXD,ND2AC,3)
      DIMENSION IAVP(NADJST)
!
      IF(LNPNM) GO TO 500
      DO 400 N=1,NAVP
      IPTP=IAVP(N)
      DO 300 K=1,3
      DO 200 I=1,ND1
      DO 100 J=1,NM
      DCDP(I,IPTP,K)=DCDP(I,IPTP,K)+PXEPA(J,IPTP,K)*ACOEF(I,J+1,K)
 100  CONTINUE
 200  CONTINUE
 300  CONTINUE
 400  CONTINUE
      RETURN
!
!
 500  CONTINUE
!
!
      DO 900 N=1,NAVP
      IPTP=IAVP(N)
      DO 800 K=1,3
      DO 700 I=1,ND1
      DO 600 J=1,NM
      DCDP(I,IPTP,K)=DCDP(I,IPTP,K)+PXEPA(IPTP,J,K)*ACOEF(I,J+1,K)
 600  CONTINUE
 700  CONTINUE
 800  CONTINUE
 900  CONTINUE
      RETURN
      END
